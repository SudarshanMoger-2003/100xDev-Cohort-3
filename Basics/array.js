const arr = [1,2,3,4,5]
//add item at the end
arr.push(6)
console.log(arr)
//remove item from end
arr.pop()
console.log(arr)
//remove item from front
arr.shift()
console.log(arr)
//add item at the beginning
arr.unshift(0)
console.log(arr)
